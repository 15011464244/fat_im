package com.ems.express.net.resp;

import java.io.Serializable;
import java.util.List;

public class QueryAddressResp implements Serializable {
	private static final long serialVersionUID = 1L;
	/**
	 * 省代码
	 */
	private String provCode;
	/**
	 * 市代码
	 */
	private String cityCode;
	/**
	 * 数据条数
	 */
	private int dataSize;
	/**
	 * 结果集
	 */
	private List<QueryAddressBean> data;

	/**
	 * @return the provCode
	 */
	public String getProvCode() {
		return provCode;
	}

	/**
	 * @param provCode
	 *            the provCode to set
	 */
	public void setProvCode(String provCode) {
		this.provCode = provCode;
	}

	/**
	 * @return the cityCode
	 */
	public String getCityCode() {
		return cityCode;
	}

	/**
	 * @param cityCode
	 *            the cityCode to set
	 */
	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}

	/**
	 * @return the dataSize
	 */
	public int getDataSize() {
		return dataSize;
	}

	/**
	 * @param dataSize
	 *            the dataSize to set
	 */
	public void setDataSize(int dataSize) {
		this.dataSize = dataSize;
	}

	/**
	 * @return the data
	 */
	public List<QueryAddressBean> getData() {
		return data;
	}

	/**
	 * @param data
	 *            the data to set
	 */
	public void setData(List<QueryAddressBean> data) {
		this.data = data;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "QueryAddressResp [provCode=" + provCode + ", cityCode="
				+ cityCode + ", dataSize=" + dataSize + ", data=" + data + "]";
	}

}
