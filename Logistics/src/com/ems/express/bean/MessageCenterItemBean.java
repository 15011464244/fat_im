package com.ems.express.bean;

import java.io.Serializable;

public class MessageCenterItemBean implements Serializable {
	private static final long serialVersionUID = 1L;

	public MessageCenterItemBean(String time, String name, String phone,
			String longitude, String latitude) {
		super();
		this.time = time;
		this.name = name;
		this.phone = phone;
		this.longitude = longitude;
		this.latitude = latitude;
	}
	/**
	 * time
	 */
	private String time;
	/**
	 * 快递员名称
	 */
	private String name;
	/**
	 * 快递员电话
	 */
	private String phone;
	/**
	 * 快递员位置：经度
	 */
	private String longitude;
	/**
	 * 快递员位置：纬度
	 */
	private String latitude;
	/**
	 * @return the time
	 */
	public String getTime() {
		return time;
	}
	/**
	 * @param time the time to set
	 */
	public void setTime(String time) {
		this.time = time;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the phone
	 */
	public String getPhone() {
		return phone;
	}
	/**
	 * @param phone the phone to set
	 */
	public void setPhone(String phone) {
		this.phone = phone;
	}
	/**
	 * @return the longitude
	 */
	public String getLongitude() {
		return longitude;
	}
	/**
	 * @param longitude the longitude to set
	 */
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	/**
	 * @return the latitude
	 */
	public String getLatitude() {
		return latitude;
	}
	/**
	 * @param latitude the latitude to set
	 */
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "MessageCenterItemBean [time=" + time + ", name=" + name
				+ ", phone=" + phone + ", longitude=" + longitude
				+ ", latitude=" + latitude + "]";
	}

}
