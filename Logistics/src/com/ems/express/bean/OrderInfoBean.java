package com.ems.express.bean;

import java.util.List;

public class OrderInfoBean {
	private String orderNo;
	private List<String> mailNo;
	private String senderName;
	private String senderPhone;
	private String senderAddress;
	private String receiveName;
	private String receivePhone;
	private String receiveAddress;
	private String weight;
	private String type;
	private String payWay;
	
	private String orderTime;
	
	public String getOrderTime() {
		return orderTime;
	}
	public void setOrderTime(String orderTime) {
		this.orderTime = orderTime;
	}
	public String getOrderNo() {
		return orderNo;
	}
	public void setOrderNo(String orderNo) {
		this.orderNo = orderNo;
	}
	public List<String> getMailNo() {
		return mailNo;
	}
	public void setMailNo(List<String> mailNo) {
		this.mailNo = mailNo;
	}
	public String getSenderName() {
		return senderName;
	}
	public void setSenderName(String senderName) {
		this.senderName = senderName;
	}
	public String getSenderPhone() {
		return senderPhone;
	}
	public void setSenderPhone(String senderPhone) {
		this.senderPhone = senderPhone;
	}
	public String getSenderAddress() {
		return senderAddress;
	}
	public void setSenderAddress(String senderAddress) {
		this.senderAddress = senderAddress;
	}
	public String getReceiveName() {
		return receiveName;
	}
	public void setReceiveName(String receiveName) {
		this.receiveName = receiveName;
	}
	public String getReceivePhone() {
		return receivePhone;
	}
	public void setReceivePhone(String receivePhone) {
		this.receivePhone = receivePhone;
	}
	public String getReceiveAddress() {
		return receiveAddress;
	}
	public void setReceiveAddress(String receiveAddress) {
		this.receiveAddress = receiveAddress;
	}
	public String getWeight() {
		return weight;
	}
	public void setWeight(String weight) {
		this.weight = weight;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPayWay() {
		return payWay;
	}
	public void setPayWay(String payType) {
		this.payWay = payType;
	}
	@Override
	public String toString() {
		return "OrderInfoBean [orderNo=" + orderNo + ", mailNo=" + mailNo
				+ ", senderName=" + senderName + ", senderPhone=" + senderPhone
				+ ", senderAddress=" + senderAddress + ", receiveName="
				+ receiveName + ", receivePhone=" + receivePhone
				+ ", receiveAddress=" + receiveAddress + ", weight=" + weight
				+ ", type=" + type + ", payWay=" + payWay + "]";
	}

	
}
