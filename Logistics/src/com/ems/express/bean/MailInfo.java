package com.ems.express.bean;

import java.util.List;
/**
 * 历史邮件信息
 * @author mingsheng
 *
 */
public class MailInfo {
	public final static String SEND_NO = "0";//未取件
	public final static String SEND_IS = "1";//实时跟踪
	
    private String sid;
    
    private String mailState;
    
	private List<String> mailNum;

	public String getSid() {
		return sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public String getMailState() {
		return mailState;
	}

	public void setMailState(String mailState) {
		this.mailState = mailState;
	}

	public List<String> getMailNum() {
		return mailNum;
	}

	public void setMailNum(List<String> mailNum) {
		this.mailNum = mailNum;
	}

	@Override
	public String toString() {
		return "MailInfo [sid=" + sid + ", mailState=" + mailState
				+ ", mailNum=" + mailNum + "]";
	}

	
	

}
