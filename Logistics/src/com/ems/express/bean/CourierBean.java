package com.ems.express.bean;

import java.io.Serializable;

public class CourierBean implements Serializable {
	private static final long serialVersionUID = 1L;
	/**
	 * 快递员编号
	 */
	private String id;
	/**
	 * 快递员头像
	 */
	private String avatar;
	/**
	 * 快递员名称
	 */
	private String name;
	/**
	 * 联系方式
	 */
	private String phone;

	public CourierBean() {
		super();
	}

	public CourierBean(String id, String avatar, String name, String phone) {
		super();
		this.id = id;
		this.avatar = avatar;
		this.name = name;
		this.phone = phone;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	@Override
	public String toString() {
		return "CourierBean [id=" + id + ", avatar=" + avatar + ", name="
				+ name + ", phone=" + phone + "]";
	}

}
