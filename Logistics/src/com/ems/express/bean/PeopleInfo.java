/**
 * Send_prov Send_city Send_county Send_prov_code Send_city_code
 * Send_county_code Slocation
 * 
 * @author chengke
 * 
 */
public class PeopleInfo implements Serializable {
	private static final long serialVersionUID = 1L;
	private String name;
	private String phone;
	private String prov;
	private String city;
	private String county;
	private String provCode;
	private String cityCode;
	private String countyCode;
	private String location;
	//类型
	private int type;
	
	private int isDef;//0非默认，1默认
	//主键
	private String id;
	//用户编号
	private String userId;
	
	private String createDate;

	public String getCreateDate() {
		return createDate;
	}

	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}


	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	@Override
	public String toString() {
		return "PeopleInfo [name=" + name + ", phone=" + phone + ", prov="
				+ prov + ", city=" + city + ", county=" + county
				+ ", provCode=" + provCode + ", cityCode=" + cityCode
				+ ", countyCode=" + countyCode + ", location=" + location
				+ ", type=" + type + ", isDef=" + isDef + "]";
	}

	public int getIsDef() {
		return isDef;
	}

	public void setIsDef(int isDef) {
		this.isDef = isDef;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getProv() {
		return prov;
	}

	public void setProv(String prov) {
		this.prov = prov;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getProvCode() {
		return provCode;
	}

	public void setProvCode(String provCode) {
		this.provCode = provCode;
	}

	public String getCityCode() {
		return cityCode;
	}

	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}

	public String getLocation() {
		return location;
	}

	public void setLocation(String location) {
		this.location = location;
	}

	public String getCounty() {
		return county;
	}

	public void setCounty(String county) {
		this.county = county;
	}

	public String getCountyCode() {
		return countyCode;
	}

	public void setCountyCode(String countyCode) {
		this.countyCode = countyCode;
	}
}
