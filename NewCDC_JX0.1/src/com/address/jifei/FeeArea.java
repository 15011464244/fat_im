package com.address.jifei;

import java.io.Serializable;

public class FeeArea implements Serializable {
	/**
	 * 计费区间表
	 * */
	private int fee_area_id;// 计费区间ID，主键ID，全表唯一索引
	private String send_area_code;// 寄件地区行政区划代码，可以省市
	private String send_area_name;// 寄件地区行政区划名称，冗余字段
	private String receive_area_code;// 收件地区行政区划代码，可以省市
	private String receive_area_name;// 计费区间ID，收件地区行政区划名称，冗余字段
	private int product_type;// 业务类型，快递包裹为1，标准快递为2

	public int getFee_area_id() {
		return fee_area_id;
	}

	public void setFee_area_id(int fee_area_id) {
		this.fee_area_id = fee_area_id;
	}

	public String getSend_area_code() {
		return send_area_code;
	}

	public void setSend_area_code(String send_area_code) {
		this.send_area_code = send_area_code;
	}

	public String getSend_area_name() {
		return send_area_name;
	}

	public void setSend_area_name(String send_area_name) {
		this.send_area_name = send_area_name;
	}

	public String getReceive_area_code() {
		return receive_area_code;
	}

	public void setReceive_area_code(String receive_area_code) {
		this.receive_area_code = receive_area_code;
	}

	public String getReceive_area_name() {
		return receive_area_name;
	}

	public void setReceive_area_name(String receive_area_name) {
		this.receive_area_name = receive_area_name;
	}

	public int getProduct_type() {
		return product_type;
	}

	public void setProduct_type(int product_type) {
		this.product_type = product_type;
	}

}
