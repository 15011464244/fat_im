package com.newcdc.tools;

public class BaiduGpsContants {

	private static BaiduGpsContants instance = null;

	private String addressStr, lat, lng;
	private String pro, city, district, street, streetNumber;
	private Long time;

	public String getAddressStr() {
		return addressStr;
	}

	public String getPro() {
		return pro;
	}

	public void setPro(String pro) {
		this.pro = pro;
	}

	@Override
	public String toString() {
		return "BaiduGpsContants [addressStr=" + addressStr + ", lat=" + lat
				+ ", lng=" + lng + ", pro=" + pro + ", city=" + city
				+ ", district=" + district + ", street=" + street
				+ ", streetNumber=" + streetNumber + ", time=" + time + "]";
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getDistrict() {
		return district;
	}

	public void setDistrict(String district) {
		this.district = district;
	}

	public String getStreet() {
		return street;
	}

	public void setStreet(String street) {
		this.street = street;
	}

	public String getStreetNumber() {
		return streetNumber;
	}

	public void setStreetNumber(String streetNumber) {
		this.streetNumber = streetNumber;
	}

	public void setAddressStr(String addressStr) {
		this.addressStr = addressStr;
	}

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getLng() {
		return lng;
	}

	public void setLng(String lng) {
		this.lng = lng;
	}

	public Long getTime() {
		return time;
	}

	public void setTime(Long time) {
		this.time = time;
	}

	public static BaiduGpsContants getInstance() {
		if (instance == null) {
			instance = new BaiduGpsContants();
		}
		return instance;
	}

}
