package com.newcdc.chat.model;

import java.io.Serializable;

public class MsgBean implements Serializable {
	private static final long serialVersionUID = 1L;
	private Long pkid;
	/**
	 * 源客户端号（发送人推送唯一标示）
	 */
	private String source;
	/**
	 * 目标客户端号（接收人推送唯一标示）
	 */
	private String target;
	/**
	 * 消息类型 0：文本消息；1：图片消息；2：音频消息
	 */
	private String message_type;
	/**
	 * 消息内容
	 */
	private String content;
	/**
	 * 资源地址
	 */
	private String url;
	/**
	 * 应用编号
	 */
	private Long app_id;
	/**
	 * 应用名称
	 */
	private String app_name;
	/**
	 * 标签编号
	 */
	private Long tag_id;
	/**
	 * 标签名称
	 */
	private String tag_name;
	/**
	 * @return the pkid
	 */
	public Long getPkid() {
		return pkid;
	}
	/**
	 * @param pkid the pkid to set
	 */
	public void setPkid(Long pkid) {
		this.pkid = pkid;
	}
	/**
	 * @return the source
	 */
	public String getSource() {
		return source;
	}
	/**
	 * @param source the source to set
	 */
	public void setSource(String source) {
		this.source = source;
	}
	/**
	 * @return the target
	 */
	public String getTarget() {
		return target;
	}
	/**
	 * @param target the target to set
	 */
	public void setTarget(String target) {
		this.target = target;
	}
	/**
	 * @return the message_type
	 */
	public String getMessage_type() {
		return message_type;
	}
	/**
	 * @param message_type the message_type to set
	 */
	public void setMessage_type(String message_type) {
		this.message_type = message_type;
	}
	/**
	 * @return the content
	 */
	public String getContent() {
		return content;
	}
	/**
	 * @param content the content to set
	 */
	public void setContent(String content) {
		this.content = content;
	}
	/**
	 * @return the url
	 */
	public String getUrl() {
		return url;
	}
	/**
	 * @param url the url to set
	 */
	public void setUrl(String url) {
		this.url = url;
	}
	/**
	 * @return the app_id
	 */
	public Long getApp_id() {
		return app_id;
	}
	/**
	 * @param app_id the app_id to set
	 */
	public void setApp_id(Long app_id) {
		this.app_id = app_id;
	}
	/**
	 * @return the app_name
	 */
	public String getApp_name() {
		return app_name;
	}
	/**
	 * @param app_name the app_name to set
	 */
	public void setApp_name(String app_name) {
		this.app_name = app_name;
	}
	/**
	 * @return the tag_id
	 */
	public Long getTag_id() {
		return tag_id;
	}
	/**
	 * @param tag_id the tag_id to set
	 */
	public void setTag_id(Long tag_id) {
		this.tag_id = tag_id;
	}
	/**
	 * @return the tag_name
	 */
	public String getTag_name() {
		return tag_name;
	}
	/**
	 * @param tag_name the tag_name to set
	 */
	public void setTag_name(String tag_name) {
		this.tag_name = tag_name;
	}
	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "MsgBean [pkid=" + pkid + ", source=" + source + ", target="
				+ target + ", message_type=" + message_type + ", content="
				+ content + ", url=" + url + ", app_id=" + app_id
				+ ", app_name=" + app_name + ", tag_id=" + tag_id
				+ ", tag_name=" + tag_name + "]";
	}

}
