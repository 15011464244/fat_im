public class LanBean {
	String mail_num;//邮件号
	String totailCharge;//实收金额
	String gekoudaima;//格口代码
	String pay_type;//支付方式
	String frequency;//频次
	String frequencyDate;//频次日期

	public String getMail_num() {
		return mail_num;
	}

	public void setMail_num(String mail_num) {
		this.mail_num = mail_num;
	}

	public String getTotailCharge() {
		return totailCharge;
	}

	public void setTotailCharge(String totailCharge) {
		this.totailCharge = totailCharge;
	}

	public String getGekoudaima() {
		return gekoudaima;
	}

	public void setGekoudaima(String gekoudaima) {
		this.gekoudaima = gekoudaima;
	}

	public String getPay_type() {
		return pay_type;
	}

	public void setPay_type(String pay_type) {
		this.pay_type = pay_type;
	}

	public String getFrequency() {
		return frequency;
	}

	public void setFrequency(String frequency) {
		this.frequency = frequency;
	}

	public String getFrequencyDate() {
		return frequencyDate;
	}

	public void setFrequencyDate(String frequencyDate) {
		this.frequencyDate = frequencyDate;
	}

	@Override
	public String toString() {
		return "LanBean [mail_num=" + mail_num + ", totailCharge="
				+ totailCharge + ", gekoudaima=" + gekoudaima + ", pay_type="
				+ pay_type + ", frequency=" + frequency + ", frequencyDate="
				+ frequencyDate + "]";
	}

}
